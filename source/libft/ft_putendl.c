#include "libft.h"

void	ft_putendl(char const *s)
{
	int q;

	q = 0;
	if (s)
	{
		while (s[q] != '\0')
		{
			ft_putchar(s[q]);
			q++;
		}
		ft_putchar('\n');
	}
}
